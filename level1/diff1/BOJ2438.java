package level1.diff1;

import java.util.*;

public class BOJ2438 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int size = scan.nextInt();
        String s = "";
        for (int i = 0; i < size; i++){
            s += "*";
            System.out.println(s);
        }
    }
}
